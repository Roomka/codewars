package com.roman.procopenco.codewars;


public class Diamond {

    /**
     * https://www.codewars.com/kata/give-me-a-diamond/train/java
     * @param n
     * @return
     */

    public static String print(int n) {
        if (n < 0 || Math.floorMod(n, 2) == 0 ) return null;

        return null;
    }
}
